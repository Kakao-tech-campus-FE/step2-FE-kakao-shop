import { instance } from "./api";

// api 요청
export const register = (data) => {
  const { email, password, username } = data;
  return instance.post("/join", {
    email,
    password,
    username,
  });
};

export const login = (data) => {
  const { email, password, username } = data;
  return instance.post("/login", {
    email,
    password,
    username,
  });
};
